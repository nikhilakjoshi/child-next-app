import Head from "next/head";
import clsx from "clsx";
import { Rubik } from "next/font/google";
import type { GetServerSidePropsContext } from "next";
import { useRouter } from "next/router";

const font = Rubik({
  subsets: ["latin-ext"],
});

export const getServerSideProps = ({ req, res }: GetServerSidePropsContext) => {
  console.log("COOKIES", req.headers.cookie);
  const cookieToken = req.headers.cookie?.split("=")[1];
  if (!cookieToken) {
    return {
      redirect: {
        destination: "/login",
        permanent: false,
      },
    };
  }
  const parsed = Buffer.from(cookieToken, "base64").toString("utf-8");
  if (parsed !== "This is a secret token") {
    return {
      redirect: {
        destination: "/login",
        permanent: false,
      },
    };
  }
  return {
    redirect: {
      destination: req.headers.location ?? "/home",
      permanent: false,
    },
  };
};

export default function Home() {
  const router = useRouter();
  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={clsx("flex h-[100dvh] flex-col", font.className)}>
        <nav className="bg-[#255be3] px-4 py-2">
          <h1 className="text-xl text-white">Citi frame</h1>
        </nav>
        <div className="grid grow place-items-center bg-green-50 px-4 py-2">
          Post Login Content - {JSON.stringify(router.asPath)}
        </div>
      </main>
    </>
  );
}
